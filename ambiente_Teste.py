pri_numero = 10
seg_numero = 20

def soma(a, b):
    """
    Função que soma dois números.
    :param a: Primeiro número
    :param b: Segundo número
    :return: Soma dos dois números
    """
    return a + b

print(pri_numero)
print(seg_numero)

resultado = soma(pri_numero, seg_numero)

def main():
    """
    Função principal que executa as operações matemáticas.
    """
    print(resultado)

if __name__ == "__main__":
    main()
